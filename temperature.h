/**
 * @brief Print a celsius to fahrenheit mapping table, from 0 to 40 Celsius
 */
int getConversions();

/**
 * @brief Convert celsius to fahrenheit, to the second decimal
 * 
 * @param celsius 
 * @return float 
 */
float celToFah(float celsius);

/**
 * @brief Convert fahrenheit to celsius, to the second decimal
 * 
 * @param fah 
 * @return float 
 */
float fahToCel(float fah);